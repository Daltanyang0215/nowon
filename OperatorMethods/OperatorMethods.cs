using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace OperatorMethods
{
    internal class OperatorMethods
    {
        static public int Sum(int a, int b)
        {
            return a + b;
        }

        static public float Sum(float a, float b)
        {
            return a + b;
        }

        static public int Sub(int a, int b)
        {
            return a + b;
        }

        static public float Sub(float a, float b)
        {
            return a + b;
        }

        static public int Div(int a, int b)
        {
            return a + b;
        }

        static public float Div(float a, float b)
        {
            return a + b;
        }

        static public int Mut(int a, int b)
        {
            return a + b;
        }

        static public float Mut(float a, float b)
        {
            return a + b;
        }

        static public bool Or(bool a, bool b)
        {
            return a | b;
        }

        static public bool And(bool a, bool b)
        {
            return a & b;
        }
        static public bool Xor(bool a, bool b)
        {
            return a ^ b;
        }
    }
}
